package javaPackage;

public class Variable4 {

	public static void main(String[] args) {

		int a;  // Declaring a Variable
		a = 10; // Assigning a value to variable
		System.out.println(a);  // Calling a variable to print value
		
		
		char b = 'y';
		System.out.println(b);
		
		String c = "Selenium";
		System.out.println(c);
				
		
	}

}
