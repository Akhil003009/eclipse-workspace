package javaPackage;

public class Variables {

	public static void main(String[] args) {
		
		int a = 5; // Local Variables
		System.out.println(a); // 5
		
		int x = 100;  // Local Variables
		System.out.println(x); // 100
		
		char c = 'a'; //char type variable can be stored in single quote
		System.out.println(c); //a
		
		boolean b = true;
		b = false; // override
		System.out.println(b); // false
		
		System.out.println(3>10); //false
		System.out.println(10>3); // true
		
		int e = 100; //Local Variable
		int f = 200; //Local Variable
		
		boolean h = e > f;
		System.out.println(h); //false
		
		
		
				

	}

}
