package javaPackage;

public class AssignmentOperator {

	public static void main(String[] args) {


		int a = 10; // local variable
		
		a += 3;
		System.out.println(a); // 13
		
		a -= 3;
		System.out.println(a); //10
		
		a *=5;
		System.out.println(a);//10*5=50
		
		a /= 10;
		System.out.println(a); // 50/10=5
	}

}
